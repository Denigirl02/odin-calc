console.log(numberAdd(5, 5));
console.log(numberSubtract(6, 2));
console.log(numberMultiply(5, 5));
console.log(numberDivide(10, 2));

// FUNCTIONS
function numberAdd(a, b) {
	const result = a + b;
	return result;
}

function numberSubtract(a, b) {
	const result = a - b;
	return result;
}

function numberMultiply(a, b) {
	const result = a * b;
	return result;
}

function numberDivide(a, b) {
	const result = a / b;
	return result;
}
